# 使用 + 来拼接字符串
fist_name = "畋"
last_name = "万"
full_name = fist_name + " " + last_name
print(full_name)


print("python")
# \t 制表符 \n 换行符
print("\tpython")
print("\n JavaScript")

# rstrip()可以删除字符串末尾多余的空白
# lstrip()可删除开头的空白
# strip()可删除两端的空白
favorite_language = 'python '
print(favorite_language)
favorite_language = favorite_language.rstrip()
print(favorite_language)

